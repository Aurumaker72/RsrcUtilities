namespace RsrcCore.Geometry.Enums;

/// <summary>
///     The alignment relative to a parent
/// </summary>
public enum Alignment
{
    /// <summary>
    ///     Aligns to the parent's start
    /// </summary>
    Start,

    /// <summary>
    ///     Aligns to the parent's cenyer
    /// </summary>
    Center,

    /// <summary>
    ///     Aligns to the parent's end
    /// </summary>
    End,

    /// <summary>
    ///     Fills the parent
    /// </summary>
    Fill
}
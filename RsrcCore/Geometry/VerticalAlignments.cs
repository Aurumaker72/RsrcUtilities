namespace RsrcCore.Geometry.Enums;

/// <summary>
///     The manner in which a control shall be aligned vertically relative to its parent
/// </summary>
public enum VerticalAlignments
{
    /// <summary>
    ///     The control aligns to the top side
    /// </summary>
    Top,

    /// <summary>
    ///     The control aligns in the center
    /// </summary>
    Center,

    /// <summary>
    ///     The control aligns to the bottom side
    /// </summary>
    Bottom,

    /// <summary>
    ///     The control stretches to its parent
    /// </summary>
    Stretch
}
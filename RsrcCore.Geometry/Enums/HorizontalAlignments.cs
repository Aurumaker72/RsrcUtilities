namespace RsrcCore.Geometry.Enums;

/// <summary>
///     The manner in which a control shall be aligned horizontally relative to its parent
/// </summary>
public enum HorizontalAlignments
{
    /// <summary>
    ///     The control aligns to the left side
    /// </summary>
    Left,

    /// <summary>
    ///     The control aligns in the center
    /// </summary>
    Center,

    /// <summary>
    ///     The control aligns to the right side
    /// </summary>
    Right,

    /// <summary>
    ///     The control stretches to its parent
    /// </summary>
    Stretch
}